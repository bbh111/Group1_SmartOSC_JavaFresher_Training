package com.company.day3.bai2;

class UnitConversion {
    double inchesToCentimeters(double inches) {
        return inches * 2.54;
    }

    double gallonsToLiters(double gallons) {
        return gallons * 3.785;
    }

    double milesToKilometers(double miles) {
        return miles * 1.609;
    }
}
